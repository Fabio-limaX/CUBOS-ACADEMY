// 1. === (Operador de comparação de igualdade)
// 2. !== (Operador de comparação de diferença)
// 3. <   (Operador de comparação de menor que)
// 4. >   (Operador de comparação de maior que)
// 5. >=  (Operador de comparação de maior ou igual)
// 6. <=  (Operador de comparação de menor ou igual)

let numero1 = 12 
let numero2 = 15

let comparacao = numero1 === numero2
comparacao = numero1 !== numero2
comparacao = numero1 < numero2
comparacao = numero1 > numero2
comparacao = numero1 >= numero2
comparacao = numero1 <= numero2

